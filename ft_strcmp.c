#include "libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	int	q;

	q = 0;
	while (s1[q] == s2[q] && (s1[q] != '\0' && s2[q] != '\0'))
		q++;
	return ((unsigned char)s1[q] - (unsigned char)s2[q]);
}
