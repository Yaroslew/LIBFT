#include "libft.h"

void		ft_bzero(void *s, size_t n)
{
	char	*first;
	size_t	q;

	q = 0;
	first = (char*)s;
	if (n == 0)
		return ;
	while (q != n)
	{
		first[q] = '\0';
		q++;
	}
}
