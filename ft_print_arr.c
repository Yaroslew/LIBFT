#include "libft.h"

void	ft_print_arr(char **string)
{
	int	q;
	int r;

	q = 0;
	r = 0;
	while (string[q])
	{
		while (string[q][r])
		{
			putchar(string[q][r]);
			r++;
		}
		r = 0;
		q++;
	}
}
