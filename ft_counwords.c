#include "libft.h"

int		ft_counwords(const char *s)
{
	int	sum;
	int	q;

	sum = 0;
	q = 0;
	if (!s)
		return (0);
	while (s[q])
	{
		if ((s[q] != ' ' && s[q] != '\t' && s[q] != '\r' && s[q] != '\n') &&
				(s[q + 1] == ' ' || s[q + 1] == '\t' || s[q + 1] == 'r' ||
				s[q + 1] == '\n' || s[q + 1] == '\0'))
			sum++;
		q++;
	}
	return (sum);
}
